#include<iostream>
using namespace std;
int main(){
    int a,b;
    cout<<"Enter the first number :";
    cin>>a;
    cout<<"Enter the second number :";
    cin>>b;
    cout<<"Sum is :"<<a+b<<endl;
    cout<<"Substraction :"<<a-b<<endl;
    cout<<"Multiplication is :"<<a*b<<endl;
    cout<<"Division is :"<<a/b<<endl;
    cout<<"Modulus is :"<<a%b<<endl;

    return 0;
}